# include <iostream>

using namespace std;

int main()
{
    cout << 5 + 3 << endl;

    cout << 5 - 3 << endl;

    cout << 5 * 3 << endl;

    cout << 5 % 3 << endl;

    cout << 5 / 3 << endl;

    // Operation order
    cout << 5 + 3 * 10 << endl;

    cout << (5 + 3) * 10 << endl;
    


    return 0;
}